<template>
  <div id="snackbar" v-if="show" :class="{'show': show}">{{ message }}</div>
</template>

<script>
export default {
  name: 'snackbar',
  data() {
    return {
      show: false,
      message: ''
    }
  },
  created() {
    this.$store.watch(
      state => state.snackbar.snack,
      () => {
        const msg = this.$store.state.snackbar.snack
        if (msg !== '') {
          this.show = true
          this.message = this.$store.state.snackbar.snack
          this.$store.commit('snackbar/setSnack', '')
          setTimeout(() => {
            this.show = false
          }, 2500)
        }
      }
    )
  }
}
</script>

